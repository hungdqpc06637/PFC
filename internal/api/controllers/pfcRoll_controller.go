type PFCRollController struct {
	*BaseController
}

var PFCRoll = &PFCRollController{}

func (c *PFCRollController) InsertPFCRoll(ctx *gin.Context) {
	var requestParams types.PFC_Roll
	if err := ctx.ShouldBind(&requestParams); err != nil {
		log.Printf("Invalid form data: %v", err)
		response.FailWithDetailed(ctx, http.StatusBadRequest, nil, "Invalid form data")
		return
	}

	result, err := services.PFCRoll.InsertNewPFCRoll(&requestParams)

	if err != nil {
		response.FailWithDetailed(ctx, http.StatusInternalServerError, nil, err.Error())
		return
	}

	response.OkWithData(ctx, result)
}

